package com.example.campusalley;

public class Oothers {
    String title;
    String info;

    public Oothers(String title, String info) {
        this.title = title;
        this.info = info;
    }

    public Oothers() {
    }

    public String getTitle() {
        return title;
    }

    public String getInfo() {
        return info;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public void setInfo(String info) {
        this.info = info;
    }
}
